package lekcje.lekcja1.zadania.zadanie5;

public class Wlasciciel {
    private String imie;
    private String nazwisko;

    public Wlasciciel(String _imie, String _nazwisko){
        imie = _imie;
        nazwisko = _nazwisko;
    }

    public String getImie() {
        return imie;
    }

    public String getNazwisko() {
        return nazwisko;
    }
}
